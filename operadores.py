#parte 2.1 - Operadores matemáticos

#Adição
n1 = 3
n2 = 8
soma = n1 + n2
print(f'Soma: {soma}') # template string

# Subtração
n1 = 14
n2 = 5
result = n1 - n2
print(f'Resultado: {result}')

# Multiplicação
result = n1 * n2
print(f'{n1} vezes {n2} [é igual a {result}]')

# Divisão que pega número quebrado
result = n1 / n2
print(f'Resultado: {result}')

# Divisão exata só pega número inteiro
result = n1 // n2
print(f'O resto da divisão de {n1} por {n2}: {result}')

